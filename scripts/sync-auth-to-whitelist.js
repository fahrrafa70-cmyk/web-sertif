/**
 * Sync Auth Users to Whitelist
 * 
 * This script syncs all users from auth.users to email_whitelist table.
 * Users not in whitelist will be added with default 'user' role.
 * Users already in whitelist will keep their existing role.
 */

require('dotenv').config({ path: '.env.local' });
const { createClient } = require('@supabase/supabase-js');

// Environment variables
const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const SUPABASE_SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!SUPABASE_URL || !SUPABASE_SERVICE_KEY) {
  console.error('‚ùå Missing environment variables:');
  console.error('   - NEXT_PUBLIC_SUPABASE_URL');
  console.error('   - SUPABASE_SERVICE_ROLE_KEY');
  console.error('\nüí° Make sure .env.local file exists with these variables');
  process.exit(1);
}

const supabase = createClient(SUPABASE_URL, SUPABASE_SERVICE_KEY, {
  auth: {
    autoRefreshToken: false,
    persistSession: false
  }
});

/**
 * Get all users from auth.users
 */
async function getAuthUsers() {
  try {
    const { data, error } = await supabase.auth.admin.listUsers();
    
    if (error) {
      throw new Error(`Failed to fetch auth users: ${error.message}`);
    }

    return data.users || [];
  } catch (error) {
    console.error('Error fetching auth users:', error);
    throw error;
  }
}

/**
 * Get all emails from whitelist
 */
async function getWhitelistEmails() {
  try {
    const { data, error } = await supabase
      .from('email_whitelist')
      .select('email, role');

    if (error) {
      throw new Error(`Failed to fetch whitelist: ${error.message}`);
    }

    // Create a map for quick lookup
    const emailMap = new Map();
    (data || []).forEach(item => {
      emailMap.set(item.email.toLowerCase(), item.role);
    });

    return emailMap;
  } catch (error) {
    console.error('Error fetching whitelist:', error);
    throw error;
  }
}

/**
 * Sync auth users to whitelist
 */
async function syncAuthToWhitelist(dryRun = false) {
  try {
    console.log('\n‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó');
    console.log('‚ïë         Sync Auth Users to Whitelist                      ‚ïë');
    console.log('‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù\n');

    if (dryRun) {
      console.log('üîç DRY RUN MODE - No changes will be made\n');
    }

    // Step 1: Get all auth users
    console.log('üìã Step 1: Fetching auth users...');
    const authUsers = await getAuthUsers();
    console.log(`‚úÖ Found ${authUsers.length} users in auth.users\n`);

    if (authUsers.length === 0) {
      console.log('üì≠ No users found in auth.users\n');
      return;
    }

    // Step 2: Get whitelist
    console.log('üìã Step 2: Fetching whitelist...');
    const whitelistMap = await getWhitelistEmails();
    console.log(`‚úÖ Found ${whitelistMap.size} emails in whitelist\n`);

    // Step 3: Find users not in whitelist
    console.log('üìã Step 3: Analyzing differences...\n');
    
    const usersToAdd = [];
    const usersAlreadyInWhitelist = [];

    authUsers.forEach(user => {
      if (!user.email) return;
      
      const normalizedEmail = user.email.toLowerCase().trim();
      
      if (whitelistMap.has(normalizedEmail)) {
        usersAlreadyInWhitelist.push({
          email: normalizedEmail,
          role: whitelistMap.get(normalizedEmail)
        });
      } else {
        usersToAdd.push({
          email: normalizedEmail,
          role: 'member' // Default role (database constraint: admin, team, member)
        });
      }
    });

    // Display results
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log('ANALYSIS RESULTS');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

    console.log(`üìä Total auth users: ${authUsers.length}`);
    console.log(`‚úÖ Already in whitelist: ${usersAlreadyInWhitelist.length}`);
    console.log(`‚ûï Need to add: ${usersToAdd.length}\n`);

    if (usersAlreadyInWhitelist.length > 0) {
      console.log('‚úÖ ALREADY IN WHITELIST:');
      console.log('‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ');
      usersAlreadyInWhitelist.forEach((user, i) => {
        console.log(`   ${i + 1}. ${user.email} (${user.role})`);
      });
      console.log('');
    }

    if (usersToAdd.length > 0) {
      console.log('‚ûï WILL BE ADDED TO WHITELIST (with role: member):');
      console.log('‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ');
      usersToAdd.forEach((user, i) => {
        console.log(`   ${i + 1}. ${user.email} (role: member)`);
      });
      console.log('');
    } else {
      console.log('‚úÖ All auth users are already in whitelist!\n');
      return;
    }

    // Step 4: Add to whitelist (if not dry run)
    if (!dryRun) {
      console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
      console.log('ADDING TO WHITELIST');
      console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

      let successCount = 0;
      let errorCount = 0;

      for (const user of usersToAdd) {
        try {
          const { error } = await supabase
            .from('email_whitelist')
            .insert([{ email: user.email, role: user.role }]);

          if (error) {
            console.error(`‚ùå Failed to add ${user.email}: ${error.message}`);
            errorCount++;
          } else {
            console.log(`‚úÖ Added ${user.email} with role '${user.role}'`);
            successCount++;
          }
        } catch (err) {
          console.error(`‚ùå Error adding ${user.email}:`, err);
          errorCount++;
        }
      }

      console.log('\n‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
      console.log('SYNC COMPLETE');
      console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');
      console.log(`‚úÖ Successfully added: ${successCount}`);
      console.log(`‚ùå Failed: ${errorCount}`);
      console.log(`üìä Total processed: ${usersToAdd.length}\n`);

    } else {
      console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
      console.log('DRY RUN COMPLETE - No changes made');
      console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');
      console.log('üí° Run without --dry-run flag to apply changes:\n');
      console.log('   npm run sync:auth-whitelist\n');
    }

  } catch (error) {
    console.error('\n‚ùå Error during sync:', error.message);
    process.exit(1);
  }
}

/**
 * Verify sync status
 */
async function verifySync() {
  try {
    console.log('\n‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó');
    console.log('‚ïë         Verify Auth-Whitelist Sync Status                 ‚ïë');
    console.log('‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù\n');

    const authUsers = await getAuthUsers();
    const whitelistMap = await getWhitelistEmails();

    const missing = [];
    authUsers.forEach(user => {
      if (!user.email) return;
      const normalizedEmail = user.email.toLowerCase().trim();
      if (!whitelistMap.has(normalizedEmail)) {
        missing.push(normalizedEmail);
      }
    });

    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log('VERIFICATION RESULTS');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

    console.log(`üìä Total auth users: ${authUsers.length}`);
    console.log(`üìä Total whitelist entries: ${whitelistMap.size}`);
    console.log(`‚ùå Missing from whitelist: ${missing.length}\n`);

    if (missing.length > 0) {
      console.log('‚ùå USERS NOT IN WHITELIST:');
      console.log('‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ');
      missing.forEach((email, i) => {
        console.log(`   ${i + 1}. ${email}`);
      });
      console.log('\nüí° Run sync to add them:');
      console.log('   npm run sync:auth-whitelist\n');
    } else {
      console.log('‚úÖ ALL AUTH USERS ARE IN WHITELIST!\n');
    }

  } catch (error) {
    console.error('\n‚ùå Error during verification:', error.message);
    process.exit(1);
  }
}

// Main execution
async function main() {
  const args = process.argv.slice(2);
  const command = args[0];
  const isDryRun = args.includes('--dry-run');

  if (command === 'verify') {
    await verifySync();
  } else if (command === 'sync' || !command) {
    await syncAuthToWhitelist(isDryRun);
  } else {
    console.log('\nUsage:');
    console.log('  npm run sync:auth-whitelist              - Sync auth users to whitelist');
    console.log('  npm run sync:auth-whitelist:dry          - Dry run (no changes)');
    console.log('  npm run sync:auth-whitelist:verify       - Verify sync status');
    console.log('\nExamples:');
    console.log('  npm run sync:auth-whitelist              # Add missing users');
    console.log('  npm run sync:auth-whitelist:dry          # Preview changes');
    console.log('  npm run sync:auth-whitelist:verify       # Check status');
    console.log('');
  }
}

main();
